import turtle as t

t.shape("turtle")
t.speed(0)
t.pensize(20)

t.circle(100)

t.up()
t.forward(200)
t.down()
t.pencolor("red")
t.circle(100)

t.up()
t.backward(400)
t.down()
t.pencolor("blue")
t.circle(100)

t.up()
t.setheading(260)
t.down()
t.pencolor("yellow")
t.circle(100)

t.up()
t.setheading(100)
t.goto(170,0)
t.down()
t.pencolor("green")
t.circle(100)

t.done()